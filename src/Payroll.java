    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        wrkName.setText("");
        wrkDept.setText("");
        hrsWrked.setText(""); //Clear all text

        String name = JOptionPane.showInputDialog(this, "Enter Worker Name");
        if (!name.isEmpty() && name.length() >= 4) {
            wrkName.setText(name.toUpperCase());
            hrlyRate.setText("₦ 1900");
            
            String dept = JOptionPane.showInputDialog(this, "Enter Worker Department");

            if (!dept.isEmpty() && dept.length() >= 4) {
                wrkDept.setText(dept.toUpperCase());

                hrs = Integer.parseInt(JOptionPane.showInputDialog(this, "Enter Hours Worked"));

                if (hrs > 0) {
                    hrsWrked.setText(Integer.toString(hrs) + " HOURS");
                    bonus.setText("");
                    tax.setText("");
                    gross.setText("");
                    net.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Hours should be greater than 0 \n Enter a valid amount of hours worked",
                            "Error Occured", JOptionPane.ERROR_MESSAGE);
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Worker's department should be more than 4 Characters. \n Enter worker's department again",
                        "Error Occured", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Worker's name should be more than 4 Characters. \n Enter worker's name again",
                    "Error Occured", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

