    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (hrs > 90) {
            wrkName.setText("");
            wrkDept.setText("");
            hrsWrked.setText("");
            JOptionPane.showMessageDialog(this, "You are not allowed to work more than 90 hours",
                    "Error Occured", JOptionPane.ERROR_MESSAGE);
            return; //GPT Helps
        }

        int hrlyRate = 1900;
        double bonusVal, taxVal, grossPay, netPay;

        grossPay = (double) hrlyRate * hrs;
        bonusVal = 0.01 * grossPay;

        if (hrs >= 50) {
            grossPay += bonusVal;
            bonus.setText(String.format("+₦ %.0f", bonusVal)); // Format to two decimal places
        } else {
            grossPay += 0;
            bonus.setText("₦ 0.0"); // Format to two decimal places
        }

        taxVal = 0.05 * grossPay;
        netPay = grossPay - taxVal; //GPT Helps

        tax.setText(String.format("-₦ %.0f", taxVal)); // Format to two decimal places

        gross.setText(String.format("₦ %.0f", grossPay)); // Format to two decimal places
        net.setText(String.format("₦ %.0f", netPay)); // Format to two decimal places
    }//GEN-LAST:event_jButton1ActionPerformed

